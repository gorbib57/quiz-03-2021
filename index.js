var app = new Vue({
  el: "#app",
  data() {
    return {
      image: null,
      switch: null,
      images: [
      	{
          src: "images/1/1.jpg",
        },      
      	{
          src: "images/1/1a.jpg",
        },
      ]
    }
  },
  mounted() {
    this.image = this.images[0];
  },
  methods: {
    switchImage() {
      this.image = this.images[1];
    }
  }
});

